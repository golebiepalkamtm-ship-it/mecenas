-- 13. Trigger dla knowledge_base_legal
CREATE OR REPLACE FUNCTION knowledge_base_legal_tsv_trigger()
RETURNS trigger AS $$
BEGIN
    NEW.content_tsv := to_tsvector('simple', coalesce(NEW.content, ''));
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_knowledge_base_legal_tsv ON knowledge_base_legal;
CREATE TRIGGER trg_knowledge_base_legal_tsv
BEFORE INSERT OR UPDATE OF content ON knowledge_base_legal
FOR EACH ROW EXECUTE FUNCTION knowledge_base_legal_tsv_trigger();

