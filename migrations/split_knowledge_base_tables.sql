-- 11. Funkcje FTS dla knowledge_base_legal
CREATE OR REPLACE FUNCTION search_knowledge_legal_fts(
    search_query text,
    result_limit int DEFAULT 5
)
RETURNS TABLE (
    id bigint,
    content text,
    metadata jsonb,
    rank real
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        kb.id,
        kb.content,
        kb.metadata,
        ts_rank(kb.content_tsv, plainto_tsquery('simple', search_query)) AS rank
    FROM knowledge_base_legal kb
    WHERE kb.content_tsv @@ plainto_tsquery('simple', search_query)
    ORDER BY rank DESC
    LIMIT result_limit;
END;
$$;

