-- 1. Utwórz tabelę knowledge_base_legal z tą samą strukturą
CREATE TABLE knowledge_base_legal (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content text NOT NULL,
    metadata jsonb DEFAULT '{}'::jsonb,
    embedding vector(1536),
    content_tsv tsvector
);

-- 2. Utwórz tabelę knowledge_base_user z tą samą strukturą
CREATE TABLE knowledge_base_user (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content text NOT NULL,
    metadata jsonb DEFAULT '{}'::jsonb,
    embedding vector(1536),
    content_tsv tsvector
);

-- 3. Skopiuj dane z knowledge_base do knowledge_base_legal
-- (zakładamy, że istniejące dane to dane prawne)
INSERT INTO knowledge_base_legal (content, metadata, embedding, content_tsv)
SELECT content, metadata, embedding, content_tsv
FROM knowledge_base
WHERE metadata->>'category' = 'rag_legal' OR metadata->>'category' IS NULL;

-- 4. Skopiuj dane z knowledge_base do knowledge_base_user
-- (dla dokumentów użytkowników)
INSERT INTO knowledge_base_user (content, metadata, embedding, content_tsv)
SELECT content, metadata, embedding, content_tsv
FROM knowledge_base
WHERE metadata->>'category' = 'user_docs';

-- 5. Dodaj indeksy wektorowe dla knowledge_base_legal
CREATE INDEX idx_knowledge_base_legal_embedding
ON knowledge_base_legal USING ivfflat (embedding vector_cosine_ops)
WITH (lists = 100);

-- 6. Dodaj indeksy wektorowe dla knowledge_base_user
CREATE INDEX idx_knowledge_base_user_embedding
ON knowledge_base_user USING ivfflat (embedding vector_cosine_ops)
WITH (lists = 100);

-- 7. Dodaj indeksy FTS dla knowledge_base_legal
CREATE INDEX idx_knowledge_base_legal_tsv
ON knowledge_base_legal USING GIN (content_tsv);

-- 8. Dodaj indeksy FTS dla knowledge_base_user
CREATE INDEX idx_knowledge_base_user_tsv
ON knowledge_base_user USING GIN (content_tsv);

-- 9. Funkcja RPC dla knowledge_base_legal
CREATE OR REPLACE FUNCTION match_knowledge_legal(
    query_embedding vector(1536),
    match_threshold float DEFAULT 0.5,
    match_count int DEFAULT 10
)
RETURNS TABLE (
    id bigint,
    content text,
    metadata jsonb,
    similarity float
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        kb.id,
        kb.content,
        kb.metadata,
        1 - (kb.embedding <=> query_embedding) AS similarity
    FROM knowledge_base_legal kb
    WHERE 1 - (kb.embedding <=> query_embedding) > match_threshold
    ORDER BY kb.embedding <=> query_embedding
    LIMIT match_count;
END;
$$;

-- 10. Funkcja RPC dla knowledge_base_user
